    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed

        // Get the booking ID entered in the bookingIDtxt field and convert it to an integer
        int bookingID = Integer.parseInt(bookingIDtxt.getText());

        // Get the room ID entered in the roomIDtxt field
        String roomID = roomIDtxt.getText();

        try {
            // Check if the entered bookingID already exists in the list of bookings
            for (bookingClass booking : AppLib.bookingClasslst) {
                if (booking.getBookingID() == bookingID) {
                    // Display an error message if the bookingID already exists
                    JOptionPane.showMessageDialog(rootPane, "***BookingID already exists***\nPlease try again", "Error Message", JOptionPane.ERROR_MESSAGE);
                }
            }

            // Get the day, month, and year components from the corresponding text fields
            int day = Integer.parseInt(dateTxt.getText());
            int month = Integer.parseInt(monthTxt.getText());
            int year = Integer.parseInt(yearTxt.getText());

            // Create a formatted date string using the day, month, and year
            String date = day + "/" + month + "/" + year;

            // Get the selected time from the timeLst dropdown
            String time = timeLst.getSelectedItem().toString();

            // Get the contact person, email, and phone number entered in the text fields
            String contactPerson = personTxt.getText();
            String contactEmail = emailTxt.getText();
            String contactPhone = phoneTxt.getText();

            // Validate the entered day, month, and year against specific criteria
            if (day < 1 || day > 31) {
                JOptionPane.showMessageDialog(rootPane, "DD must be between 1 and 31", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if day is invalid
            }

            if (month < 1 || month > 12) {
                JOptionPane.showMessageDialog(rootPane, "MM must be between 1 and 12", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if month is invalid
            }

            if (year < 2024 || year > 2025) {
                JOptionPane.showMessageDialog(rootPane, "We are only taking bookings in the academic year 2024-2025\n Please change YY to correct year", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if year is invalid
            }

            // Validate that the contact person contains only letters (no digits)
            if (contactPerson.matches(".*\\d.*")) {
                JOptionPane.showMessageDialog(rootPane, "Contact Person must only contain letters", "Error Message", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if contact person contains digits
            }

            // Validate the contact email format
            if (!contactEmail.contains("@") || !contactEmail.contains(".com")) {
                JOptionPane.showMessageDialog(rootPane, "Contact Email must contain '@email.com'", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if contact email format is invalid
            }

            // Validate the contact phone number format and length
            if (contactPhone.length() != 11 || !contactPhone.matches("\\d+")) {
                JOptionPane.showMessageDialog(rootPane, "Contact Phone must contain 11 numbers only", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if contact phone number format is invalid
            }

            // Create a new bookingClass object with the validated data
            bookingClass obj = new bookingClass(bookingID, roomID, date, time, contactPerson, contactEmail, contactPhone);

            // Add the new booking to the list of bookings
            AppLib.bookingClasslst.add(obj);

            // Display a success message with details of the added booking
            JOptionPane.showMessageDialog(rootPane, "\nBooking added Successfully\n"
                    + "Booking ID: " + bookingID + "\nRoom ID: " + roomID + "\nDate: " + date
                    + "\nTime: " + time + "\nContact Person: " + contactPerson
                    + "\nContact Email: " + contactEmail + "\nContact Phone: " + contactPhone);
        } catch (Exception e) {
            // Catch any exceptions (e.g., invalid input) and display an error message
            JOptionPane.showMessageDialog(rootPane, "***Incorrect Values***\nPlease try again", "Error Message", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_submitBtnActionPerformed

