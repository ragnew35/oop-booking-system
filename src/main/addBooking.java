    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // Initialize a Random object to generate random numbers
        Random random = new Random();

        // Generate two random numbers between 0 and 99999
        int rand = random.nextInt(99999);
        int rand2 = random.nextInt(99999);

        // Set the generated random number as the text for bookingIDtxt field
        this.bookingIDtxt.setText(String.valueOf(rand));

        // Create a room ID string combining "RM-" and the second generated random number
        String roomID = "RM-" + rand2;

        // Set the roomIDtxt field with the created room ID
        this.roomIDtxt.setText(roomID);
    }//GEN-LAST:event_formWindowOpened

